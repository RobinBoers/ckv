                    <h1>Blok 2</h1>

                    <p>
                        Ik was dit blok perongeluk bij dans ingedeeld (<i
                            ><small>Ugh</small></i
                        >) omdat ik was vergeten het formulier in te vullen. Ik
                        zag er best wel tegenop.
                    </p>

                    <p>
                        We hebben het in de eerste les gehad over
                        vergankelijkheid, en we moesten opschrijven hoe we er
                        mee omgaan. Ik denk over het algemeen niet na over de
                        vergankelijkheid had het leven. Ik probeer gewoon het
                        beste uit mijn einidige leventje te halen. Het doel is
                        het grootste deel van de tijd gelukkig zijn en na mijn
                        dood een positieve impact op de wereld te hebben gehad.
                        Sommige mensen hebben een geloof en kunnen dat misschien
                        gebruiken om ermee om te gaan.
                    </p>

                    <p>
                        We zijn naar
                        <em
                            ><a
                                href="https://clubguyandroni.nl/voorstelling/fortune/"
                                >Fortune</a
                            ></em
                        >
                        van Guy and Roni gegaan. Ik vond de voorstelling echt
                        super tof! Ik was nog nooit eerder bij een
                        dansvoorstelling geweest en ik had verwacht dat het niks
                        voor mij ging zijn (ik dacht dat het ballet-achtig zou
                        zijn), maar ik vond het echt indrukwekkend.
                    </p>

                    <p>
                        <small
                            >Btw de site van Guy and Roni is echt super goed
                            ontworpen. Overzichtelijk, mooi gestyleerd en snel.
                            Hier wordt ik als webdesigner echt helemaal hyped
                            van XD.</small
                        >
                    </p>

                    <p>
                        Hieronder zie je de theateranalyse die we moesten maken.
                        Ik heb hem op dezelfde manier opgesteld als het
                        voorbeeld dat onze docent in de les liet zien:
                    </p>

                    <figure>
                        <img src="/ckv/assets/images/t-analyse.jpg" />
                        <figcaption>Figuur 1: Mijn T-analyse</figcaption>
                    </figure>

                    <p>
                        In de theateranalyse kan je zien welke punten in de
                        voorstelling me het best zijn bijgebleven, maar hier
                        nogmaals. Ik vond het erg cool toen:
                    </p>

                    <ul>
                        <li>
                            Er een man van het slagwerk met zijn mond
                            soundeffects maakte.
                        </li>
                        <li>De vrouw werd opgetild en in de lucht danste.</li>
                        <li>
                            De voice actor aan het einde van de voorstelling het
                            verhaal over een klein kind vertelde.
                        </li>
                    </ul>

                    <p>
                        Ik vond het wel heel moeilijk om mijn vinger te leggen
                        op de onderliggende betekenis van de voorstelling. Ik
                        vind dat over het algemeen een beetje gek aan CKV. Er
                        wordt van ons verwacht dat we kunnen uitleggen wat de
                        makers van "kunst" bedoelen met hun werk, terwijl dat
                        vaak helemaal niet te zien is zonder
                        achtergrondinformatie. Daarnaast hoor ik ook vaak dat
                        het de bedoeling van de makers is om ons "aan het denken
                        te zetten" en het "zelf een betekenis moeten geven".
                        Maar dan heeft de vraag "wat was de onderliggende
                        betekenis?" of "wat is de boodschap?" dus geen
                        meerwaarde, want de auteurs hadden er blijkbaar geen
                        specifieke betekenis aan gekoppeld.
                    </p>

                    <p>
                        Verder hebben we bodyclap en een wave behandeld in de
                        lessen. Ik heb alleen niet echt het idee dat we iets
                        geleerd hebben. We hebben het niet gehad over hoe je het
                        goed toepast, we hebben maar weinig voorbeelden gezien
                        en we hebben het (behalve bij de eindopdracht) ook niet
                        geoefend.
                    </p>

                    <p>
                        Ik vond het ook irritant dat de lessen continu werden
                        verstoord door een stelletje sukkels van de havo. Ik had
                        een beetje het idee dat dans het afvoerputje was voor
                        alle mensen die niet gekozen hadden en dus ingedeeld
                        waren.
                    </p>

                    <p>
                        Als eindopdracht moesten we een dans over een ritueel
                        maken. Ik zat ik een groepje met Jordy, Ali en een ander
                        meisje. We hadden allemaal helemaal niks met dans en we
                        vonden het dus ook vrij moeilijk iets goeds te bedenken.
                        Uiteindelijk hebben we Jordy tot kerstboom bestempeld en
                        hebben we het versieren van de kerstboom ons ritueel
                        gemaakt. Ik vond het presenteren van de eindopdracht
                        voor de hele klas erg ongemakkelijk, en ik ben niet van
                        plan nog een keer dans te kiezen.
                    </p>

                    <p>
                        Samenvattend vond ik de voorstelling wel leuk en vooral
                        erg indrukwekkend, maar vond ik de lessen nogal saai. De
                        eindopdracht ervaarde ik als erg ongemakkelijk. Ik denk
                        dat ik dit blok heb geleerd dat naar professionele dans
                        voorstellingen kijken erg leuk kan zijn, maar dat zelf
                        dansen voor een groep zeer kut is.
                    </p>

                    <h2 id="vorm-inhoud-en-materiaal">
                        Vorm, inhoud en materiaal
                    </h2>

                    <p>
                        De vorm van dans is beeldend met veel beweging. De
                        inhoud verschilt (net zoals altijd) omdat je er
                        verschillenden betekenissen aan kan koppelen, suggesties
                        kan doen met bewegingen of kan inspelen op
                        achtergrondmuziek. Het materiaal is altijd je eigen
                        lichaam, met optioneel nog kostuums en attributen.
                    </p>
