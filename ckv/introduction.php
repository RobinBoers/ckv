                    <h3>Wat als je de vorm of het materiaal zou veranderen?</h3>
                    <p>
                        Als je de vorm zou veranderen zou de foto een andere
                        inhoud krijgen.
                    </p>
                    <p>
                        Als je de appels anders neerlegt is er geen suggestie
                        van seksualiteit. Als je de wortels niet in het gras zou
                        hebben liggen zou het ook al veel minder emotie
                        oproepen. Ook als je de belichting aanpast zou de foto
                        al een andere &quot;vibe&quot; krijgen waardoor de
                        boodschap anders wordt overgebracht.
                    </p>
                    <p>
                        Als je met een andere lens of camera fotografeert krijg
                        je ook andere (kleur)contrasten die de boodschap
                        beïnvloeden.
                    </p>
                    <h3>
                        Hoe verschilt het materiaal en de vorm tussen de
                        foto&#39;s?
                    </h3>
                    <p>
                        Beide foto&#39;s maken gebruik van eten om seksualiteit
                        te suggereren. Wel is de eerste foto met de appels
                        zwart-wit en de foto met de wortels in kleur gemaakt.
                        Ook is de foto van de wortels in landscape gemaakt en is
                        de foto van de appels in portrait. Daardoor voelt de
                        foto van de appels &quot;krapper&quot; aan dan die met
                        de wortels, die is namelijk wat opener.
                    </p>
                    <p>
                        Het effect is wel dat ik de foto van de appels meer
                        ervaar als een symbolisch ding dan de foto van de
                        wortels.
                    </p>

