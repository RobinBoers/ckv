                    <h3>Wat als je de vorm of het materiaal zou veranderen?</h3>
                    <p>
                        Als je de vorm zou veranderen zou de performance een
                        andere uitwerking op het publiek hebben (een andere
                        emotie opbrengen).
                    </p>
                    <p>
                        Als je ipv. elkaar in de ogen aan te kijken hadden ze
                        ook een gesprek kunnen voeren aan de keukentafel, maar
                        dat roept toch andere emoties op. De performance bij de
                        deurpost had ook een totaal andere uitwerking op het
                        publiek gehad als ze niet naakt geweest waren.
                    </p>
                    <p>
                        Het materiaal ligt natuurlijk al vast, want dat is je
                        eigen lichaam, de vorm bepaalt echter wat de precieze
                        boodschap is (het aspect van de liefde) die de
                        performance laat zien (al kan de omgeving waarin de
                        performance wordt uitgevoerd — materiaal — ook wel
                        uitmaken).
                    </p>
                    <h3>
                        Hoe verschilt het materiaal en de vorm tussen de
                        performances?
                    </h3>
                    <p>
                        Het materiaal is, zoals ik hierboven als zei, bij beide
                        performances je eigen lichaam. De omgeving verschilt
                        wel, de performance van de naakte mensen in de deurpost
                        had niet gewerkt in de grote hal met stoelen (zonder de
                        deurpost), en ook niet als niemand door die deur zou
                        willen lopen.
                    </p>
                    <p>
                        Verder is de vorm natuurlijk verschillend omdat de
                        performances een ander aspect van de liefde beschrijven.
                    </p>

                    <h2 id="verschillen-tussen-de-disciplines">
                        Verschillen tussen de disciplines
                    </h2>
                    <p>
                        Bij beide disciplines stond de inhoud redelijk vast: een
                        aspect van de liefde. Het materiaal daarentegen was bij
                        performance altijd je eigen lichaam, en bij fotografie
                        was het vaker apparatuur zoals lichten, lenzen en
                        camera&#39;s. Bij fotografie kan je ook werken met wat
                        er wel/niet in frame is (vorm), bij performance heb je
                        dat aspect niet. Je kan bijvoorbeeld daarmee nadruk
                        leggen op dingen, zoals de handen in de volgende foto:
                    </p>
                    <figure>
                        <img
                            src="/ckv/assets/images/belichting-focus.jpg"
                            alt="Belichting kan nadruk leggen op het onderwerp van de foto" />
                        <figcaption>
